const poligono = 'quadrado';

switch (poligono) {
  case 'quadrado':
    console.log('Quadrado');
    break;
  case 'triangulo':
    console.log('Triangulo');
    break;
  case 'retangulo':
    console.log('Retangulo');
    break;
  case 'circulo':
    console.log('Circulo');
    break;
  default:
    console.log('Polígono não reconhecido.')
}