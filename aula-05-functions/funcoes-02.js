// funcoes anonimas

const somar = function (a, b) {
  return a + b
}

let resultado = somar(2, 4);

console.log(resultado);

// arrow function

const multi = (a, b) => a * b;

let resultado2 = multi(2, 9);

console.log(resultado2);

// arrow function com chaves

const div = (a, b) => {
  return a / b;
}

let resultado3 = div(100, 5);

console.log(resultado3);